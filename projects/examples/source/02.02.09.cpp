#include <iostream>

/////////////////////////////////////////////////////////////

int main()
{
    auto x = 0, y = 0;

//  ---------------------------------------------------------

    std::cout << "main : enter int x : "; std::cin >> x;

//  ---------------------------------------------------------

    switch (auto z = x; z) // support : compiler-explorer.com
    {
        [[likely]] case 1 :
            
        [[likely]] case 2 :

    //  --------------------------

        [[likely]] case 3 :
        {
            y = 1;
            
        //  ----------------
            
            [[fallthrough]];
        }

    //  --------------------------

        case 4 : { y = 2; break; }

        case 5 : { y = 3; break; }

    //  --------------------------
        
        [[unlikely]] default :
        {
            y = 4;

        //  ------
            
            break;
        }
    }

//  ---------------------------------------------------------

    std::cout << "main : y = " << y << '\n';
}

