#include <cassert>
#include <exception>
#include <filesystem>
#include <fstream>
#include <ios>
#include <iostream>
#include <stdexcept>
#include <string>

////////////////////////////////////////////////////////

int main()
{
    auto size = 5uz;

    auto path = "13.01.08.data";

//  --------------------------------------------------------------------------

    std::fstream stream(path, std::ios::in | std::ios::out | std::ios::trunc);

//  --------------------------------------------------------------------------
    
    for (auto i = 0uz; i < size; ++i)
    {
        stream << "aaaaa\n";
    }

//  --------------------------------------------------------------------------

    stream.seekp((size - 1) * 6, std::ios::beg);

//  --------------------------------------------------------------------------

    stream << "bbbbb" << std::endl;

//  --------------------------------------------------------------------------

    std::cout << "main : enter char : "; std::cin.get();

//  --------------------------------------------------------------------------

    stream.seekg((size - 1) * 6, std::ios::beg);

//  --------------------------------------------------------------------------

    std::string string;

//  --------------------------------------------------------------------------
            
    std::getline(stream >> std::ws, string);

//  --------------------------------------------------------------------------
            
    assert(string == "bbbbb");

//  --------------------------------------------------------------------------

    std::filesystem::remove(path);
}