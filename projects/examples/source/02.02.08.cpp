#include <iostream>

/////////////////////////////////////////////////////////////

int main()
{
    auto x = 0, y = 0;

//  ---------------------------------------------------------

    std::cout << "main : enter int x : "; std::cin >> x;

//  ---------------------------------------------------------

    if (auto z = x; z > 0) // support : compiler-explorer.com
    {
        y = 1;
    }
    else if (0 == z)
    {
        y = 2;
    }
    else
    {
        y = 3;
    }

//  ---------------------------------------------------------

    std::cout << "main : y = " << y << '\n';
}

